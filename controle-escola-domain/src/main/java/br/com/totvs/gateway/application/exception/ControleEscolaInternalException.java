package br.com.totvs.gateway.application.exception;

public abstract class ControleEscolaInternalException extends ControleEscolaException {

	
	private static final long serialVersionUID = 1L;

	public ControleEscolaInternalException() {
		super();
	}

	public ControleEscolaInternalException(String message, Throwable cause) {
		super(message, cause);
	}

	public ControleEscolaInternalException(String message) {
		super(message);
	}

	public ControleEscolaInternalException(Throwable cause) {
		super(cause);
	}

	
	
	

}
